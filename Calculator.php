<?php

class Calculator{
	protected $result;

	public function summa($num1,$num2){
		return $num1+$num2;		
		
	}
	public function  resta($num1,$num2){
		return $num1-$num2;
		
	}
	public function  multiplica($num1,$num2){ 
		return $num1*$num2;
		
	}
	public function  divide($num1,$num2){
		return $num1/$num2;
		
	}

}


?>